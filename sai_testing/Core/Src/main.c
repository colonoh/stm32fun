void fill_stereo_chunk(uint8_t *dst, const uint8_t *src, uint16_t samples)
{
    for (int i = 0; i < samples; i++) {
        dst[4 * i + 0] = src[2 * i];       // LSB
        dst[4 * i + 1] = src[2 * i + 1];   // MSB
        dst[4 * i + 2] = src[2 * i];       // LSB again
        dst[4 * i + 3] = src[2 * i + 1];   // MSB again
    }
}

